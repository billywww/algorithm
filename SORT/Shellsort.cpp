#include <stdio.h>

int sort(int *a, int n) {
	int step = 6;
	int i, j;
	int cur;
	while (step != 0) {                                         //step%2
		for (i = step - 1; i < n; i += step) {             //(n-1)/step
			cur = a[i];
			for (j = i-step; j >= 0; j-=step) {            //n/(4*step)
				if (cur > a[j]) {
					a[j + step] = cur;
					break;
				}
				else
					a[j + step] = a[j];                          //n/(4*step)
			}
			if (j < 0) {
				a[j + step] = cur;
			}
		}
		step /= 2;
	}
	return 1;
}

