int bubble(int *a, int n) {
	int i, j;//循环辅助变量
	int temp;//辅助空间
	for (i = 0; i < n-1;i++) {
		for (j = 0; j < n-1 - i; j++) {
			if (a[j] > a[j + 1]) {
				temp = a[j];
				a[j] = a[j + 1];
				a[j + 1] = temp;
			}
		}
	}
	return 1;
}
